public class Switch {
    
    public Switch(){
    }
    
    public void on() {
        System.out.println("Switch is on");
    }

    public void off() {
        System.out.println("Switch is off");
    }
    
    public void login() {
        System.out.println("Switch Login");
    }

    public void logoff() {
        System.out.println("Switch Logoff");
    }
}
